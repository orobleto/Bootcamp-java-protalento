package com.protalento.clase01;

public class OperadoresAsignacion {

	public static void main(String[] args) {
		//
		int contador = 0;
		System.out.println(contador);
		contador = contador - 1;
		System.out.println(contador);
		contador -= 1;
		System.out.println(contador);
		contador--;
		System.out.println(contador);
	
		System.out.println(--contador);
		
		--contador;
		++contador;
		contador++;
		contador--;
		//System.out.println(contador);

	}

}
