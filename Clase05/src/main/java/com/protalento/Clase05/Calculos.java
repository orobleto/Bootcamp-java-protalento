package com.protalento.Clase05;

public class Calculos {
	final double GRAVEDAD = 9.8;

	/**
	 * metodo para la suma de dos numeros enteros
	 * 
	 * @param a
	 * @param b
	 * @return int
	 */
	int suma(int a, int b) {
		return a + b;
	}

	/**
	 * metodo para la suma de tre numeros enteros
	 * 
	 * @param a
	 * @param b
	 * @param c
	 * @return int
	 */
	int suma(int a, int b, int c) {
		return a + b + c;
	}

	/**
	 * metodo para la suma de dos numeros byte
	 * 
	 * @param a
	 * @param b
	 * @return int
	 */
	int suma(byte a, byte b) {
		return a + b;
	}

	/**
	 * metodo para la suma de dos numeros decimales de doble precision
	 * 
	 * @param a
	 * @param b
	 * @return double
	 */

	double suma(double a, double b) {
		return a + b;
	}

}
